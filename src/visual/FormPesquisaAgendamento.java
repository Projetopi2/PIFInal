    private void btn_pesquisaAgendamentoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisaAgendamentoPesquisarActionPerformed

        
        
        
        //Instanciando a classe de DaoCLientes, utilizada para pesquisas, altera√ß√µes e inclus√µes no banco.

        DaoClientes daoClientes = new DaoClientes();
        
        String cpf = txt_pesquisaAgendamentoCpf.getText().replaceAll("[./-]", "");
        
        
        //Passa para o m√©todo "verificaExistencia" da classe DaoClientes, o cpf informado no textbox.
        //Se o m√©todo "verificaExistencia"retornar true, significa que o cpf foi encontrado no banco de dados

        if(daoClientes.verificaExistencia(cpf)){

            //Instanciando um cliente utilizando o m√©todo pesquisaCliente, passando como par√™matro o cpf
            //resgatado na textbox e recebendo como retorno um object Cliente.

            Cliente cliente = daoClientes.pesquisaCliente(cpf);
            
            //Setando o texto da textBox com o nome do cliente retornado no m√©todo "pesquisaCliente".

            txt_pesquisaAgendamentoCliente.setText(cliente.getNome());

            //Abrindo a visibilidade dos objetos de pesquisa.
            
            cbo_pesquisaAgendamentoUnidade.setEnabled(true);
            cbo_pesquisaAgendamentoFuncionario.setEnabled(true);
            btn_pesquisaAgendamentoFiltrar.setEnabled(true);
            
            DaoAgendamento daoAgend = new DaoAgendamento();
            
            DaoFuncionario daoFunc = new DaoFuncionario();
            
            ArrayList<Agendamento> lista = new ArrayList<>();
            
            lista = daoAgend.agendamentoPorCliente(cliente);
            
            DefaultTableModel model = (DefaultTableModel) tbl_pesquisaAgendamento.getModel();
            
            model.setNumRows(0);
            
            if(lista != null){
        
            for(Agendamento a : lista){
            
                Funcionario f = new Funcionario();
                
                f = daoFunc.pesquisaFuncionarioId(a.getIdFuncionario());
                
                Cliente c = daoClientes.pesquisaClienteId(a.getIdCliente());
            
                String nomeServico = daoFunc.pesquisaNomeServicoPorIdServico(a.getIdServico());
                                
                Date data = FormatacaoData.convertToDate(a.getDataAgendamento());
                
                model.addRow(new Object[] {a.getIdAgendamento(), 
                f.getNome(), nomeServico, FormatacaoData.convertToStringExibicao(data), a.getHoraInicio(), 
                a.getHoraFim(), a.getStatus()});
            
                }
            
            }

            

        }
        else{

            //Se o if retornar negativo, significa que n√£o foi encontrado um cliente com o cpf informado.

            //Exibe uma caixa de dialogo solicitando confirma√ß√£o ou n√£o.
            //Se o usu√°rio clicar em "Sim" retorna 0 pra variavel reply, se informado n√£o retorna 1

            String message = "Cliente n√£o cadastrado. Deseja cadastrar novo Cliente ?";
            String title = "Confirma√ß√£o";

            int reply = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION);

            if(reply == JOptionPane.YES_OPTION)
            {
                //Se escolhida a op√ß√£o "Sim", abrimos a janela de cadastro "FormNovoCliente".

                FormNovoCliente form = new FormNovoCliente(null, rootPaneCheckingEnabled);
                form.setVisible(true);
            }

        }
        
    }//GEN-LAST:event_btn_pesquisaAgendamentoPesquisarActionPerformed

