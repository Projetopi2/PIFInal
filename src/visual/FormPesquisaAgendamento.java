    private void btn_pesquisaAgendamentoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisaAgendamentoSalvarActionPerformed

        if(btn_pesquisaAgendamentoAlteraHora.isSelected() == false &&
            btn_pesquisaAgendamentoAlteraSituacao.isSelected() == false){

            JOptionPane.showMessageDialog(rootPane, "Nenhum dado alterado");

        }
        else{

            

            String message = "Deseja alterar os dados de agendamento?";
            String title = "Confirma√ß√£o";

            int reply = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION);

            if(reply == JOptionPane.YES_OPTION){

                DaoAgendamento daoAgend = new DaoAgendamento();

                DaoFuncionario daoFunc = new DaoFuncionario();

                Funcionario f = daoFunc.pesquisaFuncionario(txt_pesquisaAgendamentoFuncionarioAlterar.getText());

                Agendamento a = daoAgend.agendamentoPorIdAgendamento(Integer.parseInt(txt_pesquisaAgendamentoId.getText()));

                if(btn_pesquisaAgendamentoAlteraHora.isSelected()){

                    String horarioSelecionado = cbo_pesquisaAgendamentoAlterarHorario.getSelectedItem().toString();
        
                    System.out.println(horarioSelecionado);
        
                    Horarios h = daoAgend.horaInicioHoraFim(horarioSelecionado);

                    a.setHoraInicio(h.getHoraInicio());
                    a.setHoraFim(h.getHoraFim());

                }

                if(btn_pesquisaAgendamentoAlteraSituacao.isSelected()){

                    String situacao = cbo_pesquisaAgendamentoAlterarSituacao.getSelectedItem().toString();

                    if(cbo_pesquisaAgendamentoAlterarSituacao.getSelectedItem().toString().equalsIgnoreCase("Cancelado")){

                        "Cancelado".equalsIgnoreCase(situacao);
                        a.setStatus(situacao);
                    }
                    else if(cbo_pesquisaAgendamentoAlterarSituacao.getSelectedItem().toString().equalsIgnoreCase("Atendido")){

                        "Atendido".equalsIgnoreCase(situacao);
                        a.setStatus(situacao);

                    }
                    else if(cbo_pesquisaAgendamentoAlterarSituacao.getSelectedItem().toString().equalsIgnoreCase("Em atendimento")){

                        "Em atendimento".equalsIgnoreCase(situacao);
                        a.setStatus(situacao);

                    }
                    else if(cbo_pesquisaAgendamentoAlterarSituacao.getSelectedItem().toString().equalsIgnoreCase("Fila de espera")){

                        "Fila de espera".equalsIgnoreCase(situacao);
                        a.setStatus(situacao);

                    }
                    else if(cbo_pesquisaAgendamentoAlterarSituacao.getSelectedItem().toString().equalsIgnoreCase("Agendado")){

                        "Agendado".equalsIgnoreCase(situacao);
                        a.setStatus(situacao);

                    }
                }

                daoAgend.updateAgendamento(a);

                DefaultTableModel model = (DefaultTableModel) tbl_pesquisaAgendamento.getModel();

                model.setNumRows(0);

                ArrayList<Agendamento> lista = daoAgend.agendamentoPorIdFuncionario(f.getIdFuncionario());

                if(lista != null){

                    for(Agendamento b : lista){

                        String nomeServico = daoFunc.pesquisaNomeServicoPorIdServico(b.getIdServico());

                        Date data = FormatacaoData.convertToDate(b.getDataAgendamento());
                        
                        String dataAgendamento = FormatacaoData.convertToStringExibicao(data);
                        
                        model.addRow(new Object[] {b.getIdAgendamento(),
                            f.getNome(), nomeServico, dataAgendamento, b.getHoraInicio(),
                            b.getHoraFim(), b.getStatus()});

                }
            }
        }
        }
    }//GEN-LAST:event_btn_pesquisaAgendamentoSalvarActionPerformed

    private void btn_pesquisaAgendamentoPesquisarAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisaAgendamentoPesquisarAlterarActionPerformed

        cbo_pesquisaAgendamentoAlterarHorario.removeAllItems();
        
        cbo_pesquisaAgendamentoAlterarHorario.setEnabled(false);

        cbo_pesquisaAgendamentoAlterarSituacao.setEnabled(false);

        btn_pesquisaAgendamentoAlteraHora.setSelected(false);
        btn_pesquisaAgendamentoAlteraSituacao.setSelected(false);

        Integer idAgendamento = Integer.parseInt(txt_pesquisaAgendamentoId.getText());

        DaoAgendamento daoAgend = new DaoAgendamento();

        DaoFuncionario daoFunc = new DaoFuncionario();

        Agendamento a = daoAgend.agendamentoPorIdAgendamento(idAgendamento);

        if(a != null){

            btn_pesquisaAgendamentoSalvar.setEnabled(true);
            btn_pesquisaAgendamentoAlteraHora.setEnabled(true);
            btn_pesquisaAgendamentoAlteraSituacao.setEnabled(true);

            Funcionario f = daoFunc.pesquisaFuncionarioId(a.getIdFuncionario());

            txt_pesquisaAgendamentoFuncionarioAlterar.setText(f.getNome());

            txt_pesquisaAgendamentoServicoAlterar.setText(daoFunc.pesquisaNomeServicoPorIdServico(a.getIdServico()));

            txt_pesquisaAgendamentoData.setText(a.getDataAgendamento());

            txt_pesquisaAgendamentoHorarioInicio.setText(a.getHoraInicio());

            txt_pesquisaAgendamentoHorarioFim.setText(a.getHoraFim());

            txt_pesquisaAgendamentoSituacao.setText(a.getStatus());

        }
        else{

            JOptionPane.showMessageDialog(rootPane, "Agendamento n√£o encontrado.");

        }

    }//GEN-LAST:event_btn_pesquisaAgendamentoPesquisarAlterarActionPerformed

