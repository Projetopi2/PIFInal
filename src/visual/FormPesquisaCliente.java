    private void btn_PesquisarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PesquisarClienteActionPerformed

        
            
            clienteSelecionado = false;
            
            DaoClientes daoClientes = new DaoClientes();
            
            if(daoClientes.verificaExistencia(txt_PesqCpf.getText().replaceAll("[./-]", ""))){
            
            clienteSelecionado = true;
                
            Cliente cliente = daoClientes.pesquisaCliente(txt_PesqCpf.getText().replaceAll("[./-]", ""));
            
            txt_PesquisaCliNome.setText(cliente.getNome());
            txt_PesquisaCliSexo.setText(cliente.getSexo());
            txt_PesquisaCliTelFixo.setText(cliente.getTelFixo());
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Cliente não cadastrado");
                
                txt_PesqCpf.setText("");
                txt_PesquisaCliNome.setText("");
                txt_PesquisaCliSexo.setText("");
                txt_PesquisaCliTelFixo.setText("");
            }
            
            
            //Criando a tabela
            
            DefaultTableModel model = (DefaultTableModel) tbl_tabelaPesquisaClientes.getModel();
            
            model.setNumRows(0);
            
            Cliente clienteTabela = daoClientes.pesquisaCliente(txt_PesqCpf.getText());
            
            

            try {
            
                DaoClientes dao = new DaoClientes();
            
                ArrayList<Cliente> lista = new ArrayList<Cliente>();
            
                lista = dao.findAll();
            
                if(lista != null){
                
                    for(Cliente c : lista){
                
                        model.addRow(new Object[] {c.getNome(), 
                        c.getCpf(), c.getSexo(), c.getTelFixo()});
                
                    }
                }


            } catch (Exception e) {
                System.out.println("Erro: " + e.getMessage());
            }

            
            


    }//GEN-LAST:event_btn_PesquisarClienteActionPerformed

