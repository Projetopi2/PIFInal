    private void btn_PesqAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PesqAlterarActionPerformed

        
        if(txt_PesquisaCliNome.getText().equals("")){
            
            JOptionPane.showMessageDialog(rootPane, "Nenhum cliente selecionado");
            
        }
        
        else if(clienteSelecionado){
        
        String message = "Alterar dados do cliente ?";
        String title = "Confirmação";
        
        //Exibe caixa de dialogo solicitando confirmação ou não. 
        //Se o usuário clicar em "Sim" retorna 0 pra variavel reply, se informado não retorna 1
        
        int reply = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION)
        {
            alteracao = true;
            
            txt_PesquisaCliNome.setEditable(true);
            txt_PesquisaCliSexo.setEditable(true);
            txt_PesquisaCliTelFixo.setEditable(true);
        }
        }
        
        
    }//GEN-LAST:event_btn_PesqAlterarActionPerformed

    private void btn_PesqSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PesqSalvarActionPerformed
        
        
        if(txt_PesqCpf.getText().equals("")){
            
            JOptionPane.showMessageDialog(rootPane, "Nenhum cliente selecionado");
            
        }
        else if (alteracao == false){
            
            JOptionPane.showMessageDialog(rootPane, "Nenhum dado foi alterado");  
        }
        else if (alteracao){
            
            DaoClientes daoCliente = new DaoClientes();
            
            daoCliente.alteraCliente(txt_PesqCpf.getText(), txt_PesquisaCliNome.getText(), 
                    txt_PesquisaCliSexo.getText(), txt_PesquisaCliTelFixo.getText());
            
            JOptionPane.showMessageDialog(rootPane, "Dados alterados com sucesso");
            
            txt_PesquisaCliNome.setEditable(false);
            txt_PesquisaCliSexo.setEditable(false);
            txt_PesquisaCliTelFixo.setEditable(false);
            
        }
        
    }//GEN-LAST:event_btn_PesqSalvarActionPerformed

    private void btn_PesqAtualizarTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PesqAtualizarTabelaActionPerformed
        
        
        //Criando a tabela
            
        DefaultTableModel model = (DefaultTableModel) tbl_tabelaPesquisaClientes.getModel();
            
        model.setNumRows(0);
                       

        try {
            
            DaoClientes dao = new DaoClientes();
            
            ArrayList<Cliente> lista = new ArrayList<Cliente>();
            
            lista = dao.findAll();
            
            if(lista != null){
                
                for(Cliente c : lista){
                
                    model.addRow(new Object[] {c.getNome(), 
                    c.getCpf(), c.getSexo(), c.getTelFixo()});
                
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(FormPesquisaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_PesqAtualizarTabelaActionPerformed

