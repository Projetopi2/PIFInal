    private void btn_ServicosEmExecucaoVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ServicosEmExecucaoVerificarActionPerformed
        
        String dataAtual = FormatacaoData.getDateTime();
        
        String nomeUnidade = cbo_ServicosEmExecucaoUnidades.getSelectedItem().toString();
        
        String status = "Em atendimento";
        
        DaoAgendamento daoAgend = new DaoAgendamento();
        
        DaoFuncionario daoFunc = new DaoFuncionario();
        
        Integer idUnidade = daoAgend.pesquisaUnidadePorNomeUnidade(nomeUnidade);
        
        ArrayList<Agendamento> lista = new ArrayList<>();
        
        lista = daoAgend.pesquisaAgendamentosEmExecucaoPorUnidade(status, idUnidade, dataAtual);
        
        DefaultTableModel model = (DefaultTableModel) tbl_ServicosEmExecucao.getModel();

            model.setNumRows(0);

            if(lista != null){

                for(Agendamento a : lista){
                    
                    Funcionario f = daoFunc.pesquisaFuncionarioId(a.getIdFuncionario());

                    String nomeServico = daoFunc.pesquisaNomeServicoPorIdServico(a.getIdServico());

                    Date data = FormatacaoData.convertToDate(a.getDataAgendamento());
                        
                    String dataAgendamento = FormatacaoData.convertToString(data);
                        
                    model.addRow(new Object[] {a.getIdAgendamento(),
                        f.getNome(), nomeServico, dataAgendamento, a.getHoraInicio(),
                        a.getHoraFim(), a.getStatus()});

                }
            }
    }//GEN-LAST:event_btn_ServicosEmExecucaoVerificarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
        cbo_ServicosEmExecucaoUnidades.removeAllItems();
        
        DaoAgendamento daoAgend = new DaoAgendamento();
        
        ArrayList<String> lista = new ArrayList();
        
        lista = daoAgend.pesquisaUnidades();

        for(String s : lista){
                
            cbo_ServicosEmExecucaoUnidades.addItem(s);
                
        }
    }//GEN-LAST:event_formWindowOpened

    private void btn_ServicosEmExecucaoGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ServicosEmExecucaoGerarRelatorioActionPerformed
        
        Relatorios rel = new Relatorios();
        
        String dataAtual = FormatacaoData.getDateTime();
        
        String nomeUnidade = cbo_ServicosEmExecucaoUnidades.getSelectedItem().toString();
        
        String status = "Em atendimento";
        
        DaoAgendamento daoAgend = new DaoAgendamento();
        
        DaoFuncionario daoFunc = new DaoFuncionario();
        
        Integer idUnidade = daoAgend.pesquisaUnidadePorNomeUnidade(nomeUnidade);
        
        ArrayList<Agendamento> lista = new ArrayList<>();
        
        lista = daoAgend.pesquisaAgendamentosEmExecucaoPorUnidade(status, idUnidade, dataAtual);
        
        rel.gravarServicosEmExecucao(lista);
        
        JOptionPane.showMessageDialog(rootPane, "Relatório gerado com sucesso.");
        
    }//GEN-LAST:event_btn_ServicosEmExecucaoGerarRelatorioActionPerformed

